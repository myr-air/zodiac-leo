def process_file(path: Path, episode_id: str) -> None:
    if not path.is_file():
        return
    
    content = path.read_text(encoding="utf-8")
    original = content
    
    # 1. Apply episode-specific replacements first
    if episode_id in REPLACEMENTS:
        for old, new in REPLACEMENTS[episode_id]:
            content = content.replace(old, new)
            
    # 2. Apply general cleanup replacements
    for old, new in GENERAL_CLEANUP:
        content = content.replace(old, new)
            
    if content != original:
        path.write_text(content, encoding="utf-8")
        print(f"Updated {path.name} to Cozy Gen Z style.")
    else:
        print(f"No Cozy Gen Z style changes in {path.name}")

def main() -> None:
    for ep in EPISODES:
        sub_dir = BASE_DIR / ep / "subtitles"
        srt_path = sub_dir / f"{ep}.th.srt"
        vtt_path = sub_dir / f"{ep}.th.vtt"
        
        process_file(srt_path, ep)
        process_file(vtt_path, ep)

if __name__ == "__main__":
    main()
